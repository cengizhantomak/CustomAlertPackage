import SwiftUI

extension Color {
    static var RedButtonColor: Color {
        return Color("RedButtonColor", bundle: .module)
    }
    
    static var CancelButtonColor: Color {
        return Color("CancelButtonColor", bundle: .module)
    }
    
    static var AlertColor: Color {
        return Color("AlertColor", bundle: .module)
    }
    
    static var TextFieldColor: Color {
        return Color("TextFieldColor", bundle: .module)
    }
    
    static var BorderColor: Color {
        return Color("BorderColor", bundle: .module)
    }
}
